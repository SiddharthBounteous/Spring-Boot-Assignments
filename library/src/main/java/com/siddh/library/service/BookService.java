package com.siddh.library.service;

import com.siddh.library.dto.BookDTO;
import com.siddh.library.entity.Book;
import com.siddh.library.repository.BookRepo;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.List;
import java.util.stream.Collectors;

@Service
public class BookService {

    @Autowired
    BookRepo bookRepo;

    public BookDTO createBook(BookDTO bookDTO){
        if(bookDTO.getBookName()==null){
            throw new RuntimeException("Book Name can't be empty");
        }

        Book book = mapToEntity(bookDTO);

        // Save to DB
        Book savedBook = bookRepo.save(book);

        // Convert saved Entity back to DTO
        return mapToDTO(savedBook);
    }

    public BookDTO getBookById(Integer bookId){
        Book book=bookRepo.findById(bookId).orElseThrow(()->new RuntimeException("Book not found with given id"));
        return mapToDTO(book);
    }

    public BookDTO updateBook(Integer id,Book book){
        Book existingBook=bookRepo.findById(id).orElseThrow(()->new RuntimeException("Book not found with given id"));

        existingBook.setBookName(book.getBookName());
        existingBook.setAuthor(book.getAuthor());
        existingBook.setGenre(book.getGenre());

        Book updated=bookRepo.save(existingBook);
        return mapToDTO(updated);
    }

    public List<BookDTO> getAllBooks(){
        List<Book> books=bookRepo.findAll();
        return books.stream()
                .map(this::mapToDTO)
                .collect(Collectors.toList());
    }

    public void deleteBookById(Integer id){
        Book existingBook=bookRepo.findById(id).orElseThrow(()->new RuntimeException("Book not found with given id"));

        bookRepo.deleteById(id);
    }

    private BookDTO mapToDTO(Book book) {
        BookDTO dto = new BookDTO();
        dto.setBookId(book.getBookId()); // Ensure DTO has an ID field
        dto.setBookName(book.getBookName());
        dto.setAuthor(book.getAuthor());
        dto.setGenre(book.getGenre());
        return dto;
    }

    private Book mapToEntity(BookDTO dto) {
        Book book = new Book();
        // Note: ID is usually generated by DB, so we often skip setting it here for CREATE operations
        book.setBookName(dto.getBookName());
        book.setAuthor(dto.getAuthor());
        book.setGenre(dto.getGenre());
        return book;
    }
}
